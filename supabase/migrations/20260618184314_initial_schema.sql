-- ---------------------------------------------------------------------------
-- Helper for anonymous-safe figuritas listing (id uuid)
-- ---------------------------------------------------------------------------
drop function if exists public.get_anonymous_figuritas();
create or replace function public.get_anonymous_figuritas()
returns table (
	id uuid,
	numero integer,
	tipo text,
	nombre_jugador text,
	imagen_url text,
	seleccion text,
	created_at timestamptz
)
language sql
security definer
set search_path = public
as $$
	select f.id, f.numero, f.tipo, f.nombre_jugador, f.imagen_url, f.seleccion, f.created_at
	from public.figuritas f
	where auth.uid() is not null
		and f.user_id <> auth.uid();
$$;

revoke all on function public.get_anonymous_figuritas() from public;
grant execute on function public.get_anonymous_figuritas() to authenticated;

