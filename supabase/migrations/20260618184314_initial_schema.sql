-- FiguMatch initial schema (corregido: figuritas.id uuid)
-- Tables: profiles, figuritas, matches, kioscos, intercambios
-- Triggers: auto profile creation on signup + updated_at maintenance

create extension if not exists pgcrypto;

-- ---------------------------------------------------------------------------
-- Shared updated_at trigger function
-- ---------------------------------------------------------------------------
create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
	new.updated_at = now();
	return new;
end;
$$;

-- ---------------------------------------------------------------------------
-- profiles
-- ---------------------------------------------------------------------------
create table if not exists public.profiles (
	id uuid primary key references auth.users(id) on delete cascade,
	email text not null unique,
	nombre text,
	reputacion integer not null default 0 check (reputacion >= 0 and reputacion <= 5),
	created_at timestamptz not null default now(),
	updated_at timestamptz not null default now()
);

-- ---------------------------------------------------------------------------
-- figuritas (ID uuid)
-- ---------------------------------------------------------------------------
create table if not exists public.figuritas (
	id uuid default gen_random_uuid() primary key,
	user_id uuid not null references auth.users(id) on delete cascade,
	numero integer not null check (numero >= 1 and numero <= 678),
	tipo text not null check (tipo in ('repetida', 'faltante')),
	nombre_jugador text,
	imagen_url text,
	seleccion text,
	created_at timestamptz not null default now(),
	updated_at timestamptz not null default now()
);

-- ---------------------------------------------------------------------------
-- matches (figurita IDs uuid)
-- ---------------------------------------------------------------------------
create table if not exists public.matches (
	id bigint generated by default as identity primary key,
	user_a uuid not null references auth.users(id) on delete cascade,
	user_b uuid not null references auth.users(id) on delete cascade,
	figurita_a_id uuid not null references public.figuritas(id) on delete cascade,
	figurita_b_id uuid not null references public.figuritas(id) on delete cascade,
	tipo text not null default 'doble' check (tipo in ('doble', 'simple')),
	estado text not null default 'pendiente' check (estado in ('pendiente', 'aceptado', 'cancelado', 'completado')),
	created_at timestamptz not null default now(),
	updated_at timestamptz not null default now(),
	check (user_a <> user_b)
);

-- ---------------------------------------------------------------------------
-- kioscos
-- ---------------------------------------------------------------------------
create table if not exists public.kioscos (
	id bigint generated by default as identity primary key,
	nombre text not null,
	direccion text not null,
	latitud double precision not null,
	longitud double precision not null,
	activo boolean not null default true,
	created_at timestamptz not null default now()
);

-- ---------------------------------------------------------------------------
-- intercambios
-- ---------------------------------------------------------------------------
create table if not exists public.intercambios (
	id bigint generated by default as identity primary key,
	match_id bigint not null references public.matches(id) on delete cascade,
	kiosco_id bigint not null references public.kioscos(id) on delete restrict,
	codigo text not null unique,
	estado text not null default 'pendiente' check (estado in ('pendiente', 'retirado', 'expirado', 'cancelado')),
	fecha_expiracion timestamptz,
	created_at timestamptz not null default now(),
	updated_at timestamptz not null default now()
);

-- ---------------------------------------------------------------------------
-- Trigger: auto-create profile when a new auth user signs up
-- ---------------------------------------------------------------------------
create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
	insert into public.profiles (id, email, nombre)
	values (
		new.id,
		coalesce(new.email, ''),
		coalesce(
			new.raw_user_meta_data ->> 'nombre',
			new.raw_user_meta_data ->> 'full_name',
			new.raw_user_meta_data ->> 'name',
			split_part(coalesce(new.email, ''), '@', 1)
		)
	)
	on conflict (id) do nothing;

	return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert on auth.users
for each row
execute procedure public.handle_new_user();

